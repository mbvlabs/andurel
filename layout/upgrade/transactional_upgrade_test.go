func TestFrameworkFileRecognitionUsesAndurelVersionMarker(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	path := "internal/example/example.go"
	target := []byte("// Code generated by andurel v1.1.0; DO NOT EDIT.\npackage example\n")

	mustWriteTestFile(t, root, path, []byte(
		"// Code generated by andurel v1.0.3; DO NOT EDIT.\npackage example\n\nconst Previous = true\n",
	))
	recognized, err := recognizeWholeFileReplacement(root, path, target)
	if err != nil || !recognized {
		t.Fatalf("versioned replacement recognition = %t, err = %v", recognized, err)
	}

	recognized, err = recognizeWholeFileDeletion(root, path)
	if err != nil || !recognized {
		t.Fatalf("versioned deletion recognition = %t, err = %v", recognized, err)
	}

	mustWriteTestFile(t, root, path, []byte("package example\n\nconst UserOwned = true\n"))
	recognized, err = recognizeWholeFileReplacement(root, path, target)
	if err != nil || recognized {
		t.Fatalf("user-owned replacement recognition = %t, err = %v", recognized, err)
	}

	recognized, err = recognizeWholeFileDeletion(root, path)
	if err != nil || recognized {
		t.Fatalf("user-owned deletion recognition = %t, err = %v", recognized, err)
	}

	missingPath := "internal/example/missing.go"
	recognized, err = recognizeWholeFileReplacement(root, missingPath, target)
	if err != nil || !recognized {
		t.Fatalf("missing versioned replacement recognition = %t, err = %v", recognized, err)
	}
	recognized, err = recognizeWholeFileReplacement(
		root,
		missingPath,
		[]byte("package example\n\nconst UserOwned = true\n"),
	)
	if err != nil || recognized {
		t.Fatalf("missing user-owned replacement recognition = %t, err = %v", recognized, err)
	}
}

func TestUpgradeCleanAndDirtyWorktreeBehavior(t *testing.T) {
	cleanRoot := newUpgradeFixtureProject(t)
	clean, err := NewUpgrader(cleanRoot, UpgradeOptions{DryRun: true, TargetVersion: fixtureTargetVersion})
	if err != nil {
		t.Fatal(err)
	}
	report, err := clean.Execute()
	if err != nil || report.DirtyWorktree {
		t.Fatalf("clean dry run: report=%#v err=%v", report, err)
	}

	dirtyRoot := newUpgradeFixtureProject(t)
	mustWriteTestFile(t, dirtyRoot, "user-edit.txt", []byte("dirty\n"))
	dry, err := NewUpgrader(dirtyRoot, UpgradeOptions{DryRun: true, TargetVersion: fixtureTargetVersion})
	if err != nil {
		t.Fatal(err)
	}
	report, err = dry.Execute()
	if err != nil || !report.DirtyWorktree {
		t.Fatalf("dirty dry run: report=%#v err=%v", report, err)
	}

	real, err := NewUpgrader(dirtyRoot, UpgradeOptions{TargetVersion: fixtureTargetVersion})
	if err != nil {
		t.Fatal(err)
	}
	before := snapshotUpgradeTree(t, dirtyRoot)
	if _, err := real.Execute(); err == nil || !strings.Contains(err.Error(), "worktree is dirty") {
		t.Fatalf("dirty real upgrade error = %v", err)
	}
	assertSnapshotEqual(t, before, snapshotUpgradeTree(t, dirtyRoot))
}

